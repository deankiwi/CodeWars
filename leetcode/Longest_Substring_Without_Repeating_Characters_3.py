

class Solution:
    def lengthOfLongestSubstring(self, s: str) -> int:
        longest = 0
        current = ""
        for i in s:
            if i not in current:
                current += i

            else:

                current = current[current.index(i) + 1 :] + i

            longest = max(longest, len(current))

        return longest


print(Solution().lengthOfLongestSubstring("abcabcbb"), "3")
