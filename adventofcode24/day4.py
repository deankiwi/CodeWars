def day4pt1(data: str):
    # print(data)
    count = 0
    count += len(re.findall(r"(\\n)", data))
    print(count)
    count += len(re.findall(r"(XMAS)", data))
    # print(count)
    count += len(re.findall(r"(SAMX)", data))
    # print(count)

    arr = [[ch for ch in t] for t in data.split("\n")]
    # print(arr)

    def down(x, y):
        word = ""
        try:
            for i in range(y, y + 4):
                word += arr[i][x]
        except:
            return 0
        if word in ["XMAS", "SAMX"]:
            # print("Hit down", word)
            return 1
        return 0

    def dagr(x, y):
        word = ""
        try:
            for i in range(4):
                word += arr[y + i][x + i]
        except:
            return 0
        # print(word)
        if word in ["XMAS", "SAMX"]:
            # print("Hit dag r", word)
            return 1

        return 0

    def dagl(x, y):
        word = ""
        try:
            for i in range(4):
                if y-i < 0 or x-i < 0:
                    return 0
                word += arr[max(0, y - i)][max(0, x - i)]
        except:
            return 0
        # print(word)
        if word in ["XMAS", "SAMX"]:
            # print("Hit dagl", word)

            return 1

        return 0

    for y in range(len(arr)):
        for x in range(len(arr[0])):

            count += down(x, y)

            count += dagr(x, y)
            count += dagl(x, y)

    return count


